void SleepMilliseconds(_UINT32 nMilliseconds)
{
//For windows.
#ifdef _MICROSOFT_WINDOWS

	//Sleep
	Sleep(nMilliseconds);

#endif

//For linux.
#ifdef _REDHAT_LINUX

	//Sleep
	usleep(nMilliseconds * 1000);

#endif
}
